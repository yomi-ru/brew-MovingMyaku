int color(int x) {
    switch (x) {
        case 0:  return 16;
        case 1:  return 1;
        case 7:  return 7;
        case 21: return 21;
        case 33: return 33;
        default: return 16;  // 黒（fallback）
    }
}

void dote(int k) {
    printf("\e[H\e[J"); // 画面クリア
    for (int i = 0; i < 16; i++) {
        printf("%*s", k, ""); // k 個の空白（横スライド）
        for (int j = 0; j < 16; j++) {
            int x = (k % 2 == 1) ? b[i][j] : a[i][j];
            printf("\e[48;5;%dm", color(x));
            printf("　");
            printf("\e[0m");
        }
        printf("\n");
    }
    fflush(stdout);
    usleep(125000); // 0.125 秒待機
}

int main(void) {
    for (int k = 0; k < 55; k++) dote(k);  // 右に移動
    for (int k = 55; k > 0; k--) dote(k);  // 左に戻る
    return 0;
}
